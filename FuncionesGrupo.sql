SELECT EMP_CARGO, AVG(EMP_SALARIO) AS PROMEDIO
FROM EMPLEADOS
GROUP BY EMP_CARGO
HAVING AVG(EMP_SALARIO) > 1200000;

SELECT MIN(E.EMP_FECHACONTRATO)AS CONTRATOMIN, MAX(E.EMP_FECHACONTRATO)AS CONTRATOMAX 
FROM EMPLEADOS E;

SELECT MIN(E.EMP_NOMBRE) AS "NOMBRE MINIMO ORDENADO POR ORDEN ALFABETICO", 
       MAX(E.EMP_NOMBRE) AS "NOMBRE MAXIMO ORDENADO POR ORDEN ALFABETICO"
FROM EMPLEADOS E;

SELECT MIN(E.EMP_FECHACONTRATO) AS "FECHA CONTRATO MINIMA",
       MAX(E.EMP_FECHACONTRATO) AS "FECHA CONTRATO MAXIMA"
FROM EMPLEADOS E;

--ROUND REDONDEA EL VALOR DECIMAL Y SE PONE UN ,n PARA LA CANTIDAD DE DECIMALES
SELECT SUM(E.EMP_SALARIO) AS "SUMA DE LOS SALARIOS", ROUND(AVG(E.EMP_SALARIO),2) AS "PROMEDIO DE LOS SALARIOS",
       MIN(E.EMP_SALARIO) AS "SALARIO MINIMO", MAX(E.EMP_SALARIO) AS "SALARIO MAXIMO"
FROM EMPLEADOS E;

