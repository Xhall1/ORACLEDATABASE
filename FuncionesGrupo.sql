SELECT COUNT(DISTINCT(EMP_JEFE))AS NUMERO_JEFES
FROM EMPLEADOS;

SELECT DEP_ID, EMP_CARGO, SUM(EMP_SALARIO)
FROM EMPLEADOS
GROUP BY DEP_ID, EMP_CARGO;

SELECT MAX(EMP_NOMBRE) AS EMPLEADO 
FROM EMPLEADOS
WHERE EMP_FECHACONTRATO IN(BETWEEN(31-DEC-2000 AND 31-DEC-2004));

SELECT EXTRACT(YEAR FROM EMP_FECHACONTRATO) AS AÑO_CONTRATADO, COUNT(DISTINCT EMP_FECHACONTRATO) AS CANTIDAD
FROM EMPLEADOS
WHERE EXTRACT(YEAR FROM EMP_FECHACONTRATO) IN (2001,2002,2003,2004)
GROUP BY EMP_FECHACONTRATO
ORDER BY EMP_FECHACONTRATO ASC;

SELECT DISTINCT EXTRACT(YEAR FROM EMP_FECHACONTRATO) AS AÑO_CONTRATADO, COUNT(*) AS CANTIDAD
FROM EMPLEADOS
WHERE EXTRACT(YEAR FROM EMP_FECHACONTRATO) IN (2001,2002,2003,2004)
GROUP BY EXTRACT(YEAR FROM EMP_FECHACONTRATO);

